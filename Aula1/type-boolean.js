// Retorna valores verdadeiros (true) ou falsos (false)
// O operador de igualdade === compara os valores atribuídos nas variáveis

const primeiroNumero = 5;
const segundoNumero = 10;
console.log(primeiroNumero === segundoNumero); //false

const numero1 = 34;
const numero2 = 34;
console.log(numero1 === numero2); // true

const texto1 = "Morgana";
const texto2 = "morgana";
console.log(texto1 === texto2); // false

const texto3 = "Morgana";
const texto4 = "Morgana";
console.log(texto3 === texto4); // true